const entityMap: { [key: string]: string } = {
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '"': '&quot;',
    "'": '&#39;',
    '/': '&#x2F;',
    '`': '&#x60;',
    '=': '&#x3D;'
};

export function sanitize(str: string): string {
    return String(str).replace(/[&<>"'`=\/]/g, (s) => entityMap[s]);
}
